package io.github.alexeychurchill.stickynotes.model;

/**
 * User class
 */

public class User {
    private int id;
    private String login;
    private String name;
    private String lastName;

    public int getId() {
        return id;
    }

    public String getLogin() {
        return login;
    }

    public String getName() {
        return name;
    }

    public String getLastName() {
        return lastName;
    }
}
